#include "libft.h"

size_t	ft_strlcpy(char *dst, char *src, size_t dstsize)
{
	size_t			srcl;

	srcl = ft_strlen(src);
	if (srcl < dstsize)
		ft_memcpy(dst, src, srcl + 1);
	else if (dstsize != 0)
	{
		ft_memcpy(dst, src, dstsize - 1);
		dst[dstsize - 1] = '\0';
	}
	return (srcl);
}
